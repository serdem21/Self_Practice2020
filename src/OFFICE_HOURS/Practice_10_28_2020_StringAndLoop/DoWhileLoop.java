package OFFICE_HOURS.Practice_10_28_2020_StringAndLoop;

public class DoWhileLoop {

    public static void main(String[] args) {
        boolean condition = false;

        while(condition){
            System.out.println("Hello Batch 21");
        }

        System.out.println("=========================================");

        do{  // think first, do while loop gets executed at least once even if the condition is false
            System.out.println("Hello Batch 21");
        }while(condition);  // do first , think later




    }
}
